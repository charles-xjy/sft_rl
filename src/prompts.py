# ==================== Phase 1: 受控图像描述 ====================
DESCRIPTION_PROMPT = """你是一位专业的遥感图像解译人员。请对这张遥感图像生成受控描述。

## 描述要求
1. 只描述图像中**清晰可见**的内容，不要猜测或推断图像外信息
2. 覆盖以下方面：
   - 场景类型：城市、乡村、农田、水体、植被、工业区、灾后区域等
   - 主要地物：建筑、道路、植被、水体、车辆、裸地、人工设施等
   - 空间关系：相邻、位于某方向、沿道路分布、被道路分隔等
   - 可计数对象：只统计清晰可见的对象，不强行计入遮挡或模糊目标
   - 不确定内容：无法确认的用途、类型或数量需要明确标记
3. 不输出行政建议、灾害成因、工程治理方案、精确坐标或精确面积
4. 如果目标不清晰，必须说明"不确定"或"无法确认"

请用清晰、客观的语言分点描述，不要使用专业术语堆砌。"""

# ==================== Phase 2: 问题生成 ====================
QUESTION_GENERATION_PROMPT = """基于这张遥感图像和图像描述，请生成{num_questions}个VQA问题。

## 图像描述
{description}

## 问题类型要求
请按以下类型分布生成问题（类型可重复，覆盖不同方面）：
- existence: 是否存在某类地物（如："图像中是否有水体？"）
- attribute: 颜色、形状、屋顶类型、材质或纹理（如："建筑屋顶是什么颜色？"）
- location: 某地物位于图像哪个区域（如："道路位于图像的哪个区域？"）
- spatial_relation: 两个地物之间的相对位置或邻接关系（如："建筑和道路是什么位置关系？"）
- counting: 清晰可见目标的数量（如："图像中有多少栋建筑？"）
- scene: 整体场景类型或主要功能区（如："这是什么类型的场景？"）
- comparison: 不同区域的密度对比（如："图像左侧和右侧哪个区域建筑更多？"）
- reasoning: 基于可见证据的简单判断（如："根据建筑和道路分布，这可能是什么功能区？"）

## 输出格式
每行一个问题，格式为：类型|问题内容
例如：
existence|图像中是否存在道路？
attribute|建筑屋顶是什么形状？

注意：
1. 问题要简短明确，不要太长
2. 问题必须是图像中可以回答的
3. 每个问题只问一个具体方面
4. 如果图像内容简单，可以适当减少问题数量，但至少生成{min_questions}个
5. 只输出问题列表，不要输出其他解释性文字"""

# ==================== Phase 3: 带分析过程的答案生成 ====================
ANSWER_GENERATION_PROMPT = """请回答这个遥感VQA问题，并按照指定格式输出。

## 问题
{question}

## 问题类型
{question_type}

## 图像描述参考
{description}

## 回答要求
1. 只依据图像中可见内容回答，不要推断图像外信息
2. 不要给出行政、工程、灾害治理建议
3. 不要编造精确坐标、面积、距离或数量
4. 如果目标不清晰，必须说明"不确定"或"无法确认"
5. 分析过程必须是可见证据链，不要写开放式内心思考

## 输出格式
<analysis>
1. 定位：说明问题关注的区域或目标
2. 观察：描述图像中可见的具体证据
3. 判断：基于观察给出结论
</analysis>
<answer>最终答案，简洁直接</answer>

## 不同类型的分析重点
- existence: 定位目标类别或区域，观察是否存在符合形态、颜色、纹理的目标，再给出存在性判断
- attribute: 定位目标地物，观察颜色、形状、纹理或屋顶特征，再给出属性
- location: 识别目标地物，判断其在整幅图中的相对方位，再用左上、右下、中央等区域描述
- spatial_relation: 分别定位两个目标，比较二者的方位、邻接、间隔关系，再给出空间关系
- counting: 确定计数对象，统计清晰可见目标；若不确定，答案中说明为估计值
- scene: 概括主要地物组成，比较建筑、道路、植被、水体等占比，再判断场景类型
- comparison: 分别观察两个区域或对象，比较密度、数量或显著程度，再给出结论
- reasoning: 只基于可见证据做短链路判断，不扩展到图像外知识

注意：只输出指定格式的内容，不要输出开场白或解释性文字。"""
