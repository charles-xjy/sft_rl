DESCRIPTION_PROMPT = """你是一位专业的遥感图像解译人员。请对这张遥感图像生成受控描述。

## 描述要求
1. 只描述图像中清晰可见的内容，不要猜测图像外信息
2. 覆盖以下方面：
   - 场景类型：城市、乡村、农田、水体、植被、工业区、灾后区域等
   - 主要地物：建筑、道路、植被、水体、车辆、裸地、人工设施等
   - 空间关系：相邻、位于某方向、沿道路分布、被道路分隔等
   - 可计数对象：只统计清晰可见的对象，不强行计入遮挡或模糊目标
   - 不确定内容：无法确认的用途、类型或数量需要明确标注
3. 不输出行动建议、灾害成因、治理方案、精确坐标或精确面积
4. 如果目标不清晰，必须说明“不确定”或“无法确认”

请用清晰、客观的语言分点描述，不要堆砌术语。"""


QUESTION_GENERATION_PROMPT = """基于这张遥感图像和图像描述，请生成{num_questions}个 VQA 问题。

## 图像描述
{description}

## 问题类型要求
- existence: 是否存在某类地物
- attribute: 颜色、形状、屋顶类型、材质或纹理
- location: 某地物位于图像哪个区域
- spatial_relation: 两个地物之间的相对位置或邻接关系
- counting: 清晰可见目标的数量或估计数量
- scene: 整体场景类型或主要功能区
- comparison: 不同区域的建筑、植被、水体或道路密度对比
- reasoning: 基于可见证据的简单判断

## 输出格式
每行一个问题，格式必须为：
类型|问题内容

例如：
existence|图像中是否存在道路？
attribute|建筑屋顶是什么颜色？

注意：
1. 问题必须简短明确
2. 问题必须能够仅依据图像回答
3. 每个问题只问一个具体方面
4. 如果图像内容较简单，可以少于 {num_questions} 个，但至少生成 {min_questions} 个
5. 只输出问题列表，不要输出解释性文字"""


ANSWER_GENERATION_PROMPT = """请回答这个遥感 VQA 问题，并严格按指定格式输出。

## 问题
{question}

## 问题类型
{question_type}

## 图像描述参考
{description}

## 回答要求
1. 只依据图像中可见内容回答，不要推断图像外信息
2. 不要给出行政、工程或治理建议
3. 不要编造精确坐标、面积、距离或数量
4. 如果目标不清晰，必须说明“不确定”或“无法确认”
5. 分析过程必须是可见证据链，不要写内心独白、任务拆解、格式检查或自我说明

## analysis 写法要求
1. 必须固定为 3 步，并使用编号 1. 2. 3.
2. 第 1 步写“定位”：说明关注的区域或目标
3. 第 2 步写“观察”：只描述图像中直接可见的证据
4. 第 3 步写“判断”：只基于前述证据给出结论，不新增图像外信息
5. 每步 1-2 句，总体保持简洁，不要超过 180 个中文字符

## 输出格式
<analysis>
1. 定位：说明问题关注的区域或目标。
2. 观察：描述图像中直接可见的证据。
3. 判断：基于上述证据给出结论。
</analysis>
<answer>最终答案，简洁直接</answer>

## 不同类型的分析重点
- existence: 定位目标类别或区域，观察是否存在对应目标，再给出存在性判断
- attribute: 定位目标地物，观察颜色、形状、纹理或屋顶特征，再给出属性结论
- location: 识别目标地物，判断其相对方位，并使用左上、右下、中部等方位词
- spatial_relation: 分别定位两个目标，比较其方位、邻接或间隔关系
- counting: 确定计数对象，统计清晰可见目标；若不确定，在答案中明确说明为估计或无法确认
- scene: 概括主要地物组成，依据整体覆盖情况判断场景类型
- comparison: 同时观察两个区域或对象，再比较密度、数量或显著程度
- reasoning: 只基于可见证据做短链路判断，不扩展到图像外知识

注意：只输出指定格式的内容，不要输出开场白或解释性文字。"""
