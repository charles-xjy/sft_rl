"""
Prompt templates used by the three-stage pipeline.

设计要点(对应 reviewer 的 5 个风险):
- Phase3 以图像为准、描述仅供参考且可能有误            -> 缓解 Risk1(描述单点真相源)
- analysis 步数按需(1-3),并提供"直答(无 analysis)"prompt -> 缓解 Risk2(过度模板化)
- 问题类型给出配比建议、压低 counting、新增 unanswerable -> 缓解 Risk3/Risk4
"""

DESCRIPTION_PROMPT = """你是一位专业的遥感图像解译人员。请对这张遥感图像生成受控描述。

## 描述要求
1. 只描述图像中清晰可见的内容，不要猜测图像外信息
2. 覆盖以下方面：
   - 场景类型：城市、乡村、农田、水体、植被、工业区、灾后区域等
   - 主要地物：建筑、道路、植被、水体、车辆、裸地、人工设施等
   - 空间关系：相邻、位于某方向、沿道路分布、被道路分隔等
   - 可计数对象：只统计清晰可见的对象，不强行计入遮挡或模糊目标
   - 不确定内容：无法确认的用途、类型或数量需要明确标注
3. 不输出行动建议、灾害成因、治理方案、精确坐标或精确面积
4. 如果目标不清晰，必须说明“不确定”或“无法确认”

请用清晰、客观的语言分点描述，不要堆砌术语。"""


QUESTION_GENERATION_PROMPT = """基于这张遥感图像生成 VQA 问题。
下面的描述仅供参考，可能有误；请以图像实际内容为准。

## 图像描述（仅供参考，可能有误）
{description}

## 本图建议生成的问题类型与数量
请按下面的清单生成问题（每类按指定数量）：
{assigned_plan}

各类型含义：
- existence: 是否存在某类地物
- location: 某地物位于图像哪个区域
- spatial_relation: 两个地物之间的相对位置或邻接关系
- attribute: 颜色、形状、屋顶类型、材质或纹理
- counting: 清晰可见、数量较少、可准确点数的目标数量；遮挡/密集/模糊场景不适合
- unanswerable: 仅凭该图像无法确认的问题（用途、材质年代、权属归属等）
- ambiguous: 图像中存在多个合理解释、难以唯一确定的问题（如“这是仓库还是厂房？”）

## 输出格式
每行一个问题，格式必须为：
类型|问题内容

例如：
existence|图像中是否存在道路？
ambiguous|图像中心那栋大跨度建筑是仓库还是厂房？

注意：
1. 尽量贴合上面的类型清单；但若某个类型确实不适合本图（如无可数目标却要 counting），可跳过该类型并减少对应问题，不要硬凑。
2. 问题简短明确，只问一个具体方面，且能仅凭图像回答（unanswerable / ambiguous 除外）。
3. 同类问题不要用相同句式起手或问同一对象，尽量覆盖不同地物、不同角度，避免“是否有建筑/道路/植被”式的雷同。
4. 只输出问题列表，不要输出解释性文字。"""


# ── 带 analysis 的回答(默认主力,约 70%) ──
ANSWER_GENERATION_PROMPT = """请回答这个遥感 VQA 问题，并按指定格式输出分析与答案。

## 问题
{question}

## 问题类型
{question_type}

## 图像描述（仅供参考，可能有误）
{description}

## 核心原则
1. 一切以图像实际内容为准。描述仅作参考；当描述与图像冲突时，以图像为准。
2. 只依据图像中可见内容回答，不推断图像外信息。
3. 不给出行政/工程/治理建议，不编造坐标、面积、距离或精确数量。
4. 若图像信息不足以判断，必须直接说明“无法根据图像确认”或“不确定”，不要强行作答。

## analysis 写法要求
1. analysis 是“可见证据链”，不是内心独白、任务拆解或格式说明。
2. 步数按问题复杂度决定：简单问题 1 步即可，复杂问题最多 3 步；不要为凑步数注水。
3. 每步先点出要点（如 定位 / 观察 / 判断）再写内容，每步 1-2 句，整体不超过 180 个中文字符。

## 输出格式
<analysis>
1. ...
2. ...（按需，可省略）
3. ...（按需，可省略）
</analysis>
<answer>最终答案，简洁直接</answer>

## 各类型分析重点
- existence: 观察对应目标是否可见后给出存在性结论（通常 1-2 步即可）
- attribute: 观察颜色、形状、纹理或屋顶特征后给出属性结论
- location: 判断目标相对方位，使用左上、右下、中部等方位词
- spatial_relation: 分别定位两个目标，比较其方位、邻接或间隔关系
- counting: 统计清晰可见目标；遮挡或模糊导致无法精确时，给出估计或说明无法确认
- unanswerable: 若图像确实无法确认，简要说明依据后，answer 给出“无法根据图像确认……”
- ambiguous: 图像存在多个合理解释时，列出主要可能性，answer 给出“无法准确区分，可能是……也可能是……”

注意：只输出指定格式的内容，不要输出开场白或解释性文字。"""


